/**
 * Replace $variable placeholders with actual values
 *
 * @param string $template Message template with $variables
 * @param array $context Contextual data (scores, dice, etc.)
 * @param array $botPlayer Bot player record
 * @return string Formatted message
 */
function Bot_FormatMessage($template, $context, $botPlayer) {
	// Build complete variable map
	$variables = [
		// Bot info
		'$bot_username' => $botPlayer['username'] ?? 'Bot',
		'$bot_level' => $botPlayer['level'] ?? 1,
		'$bot_score' => $context['bot_score'] ?? 0,
		'$bot_last_keep_score' => $context['bot_last_keep_score'] ?? 0,
		'$final_bot_score' => $context['final_bot_score'] ?? 0,

		// Player info (from context)
		'$player_username' => $context['player_username'] ?? 'Player',
		'$player_level' => $context['player_level'] ?? 1,
		'$player_score' => $context['player_score'] ?? 0,
		'$player_last_keep_score' => $context['player_last_keep_score'] ?? 0,
		'$final_player_score' => $context['final_player_score'] ?? 0,

		// Game state
		'$round' => $context['round'] ?? 1,
		'$lead' => abs($context['lead'] ?? 0),

		// Scoring
		'$points' => $context['points'] ?? 0,
		'$turn_score' => $context['turn_score'] ?? 0,

		// Dice info
		'$dice_description' => $context['dice_description'] ?? 'dice',
		'$num_dice' => $context['num_dice'] ?? 6,
		'$num_dice_left' => $context['num_dice_left'] ?? 6,
		'$farkle_prob' => $context['farkle_prob'] ?? 0,

		// Game conclusion
		'$winner' => $context['winner'] ?? '',
		'$score_difference' => $context['score_difference'] ?? 0,
		'$game_result' => $context['game_result'] ?? ''
	];

	// Replace all variables
	$message = $template;
	foreach ($variables as $var => $value) {
		$message = str_replace($var, $value, $message);
	}

	return $message;
}

/**
 * Select a message for a specific category
 *
 * @param int $category Message category (0-10)
 * @param array $botPlayer Bot player record with 'bot_algorithm' field
 * @param array $context Context variables for message substitution
 * @return string|null Formatted message or null if not found
 */
function Bot_SelectMessage($category, $botPlayer, $context = []) {
	$templates = Bot_GetMessageTemplates();
	$algorithm = $botPlayer['bot_algorithm'] ?? 'medium';

	// 5% chance of trash talk in competitive situations (categories 1, 2, 6)
	if (in_array($category, [1, 2, 6]) && rand(1, 100) <= 5) {
		$category = 8; // Trash talk
	}

	// Get messages for this category and algorithm
	if (!isset($templates[$category]) || !isset($templates[$category][$algorithm])) {
		return null;
	}

	$messageList = $templates[$category][$algorithm];
	if (empty($messageList)) {
		return null;
	}

	// Select random message
	$template = $messageList[array_rand($messageList)];

	// Format with context variables
	return Bot_FormatMessage($template, $context, $botPlayer);
}

